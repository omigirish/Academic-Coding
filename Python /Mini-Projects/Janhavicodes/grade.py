m=int(input("Enter marks:"))
if m>0 and m<=40:
    print("Grade=F")
elif m>=41 and m<=50:
    print("Grade=E")
elif m >=51 and m <= 60:
    print("Grade=D")
elif m >= 61 and m <= 70:
    print("Grade=C")
elif m >= 71 and m <=80:
    print("Grade=B")
elif m >= 81 and m <= 100:
    print("Grade=A")

